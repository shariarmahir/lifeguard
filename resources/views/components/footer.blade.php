<script>
const debounce = (func, wait) => {
    let timeout;
    return (...args) => {
        clearTimeout(timeout);
        timeout = setTimeout(() => func.apply(this, args), wait);
    };
};

const subscribeNewsletter = debounce(async () => {
    const emailInput = document.querySelector('input[type="email"]');
    const email = emailInput.value.trim();
    const errorEl = document.querySelector('[x-text="error"]');

    if (!email) {
        Alpine.store('newsletter', { error: 'Please enter a valid email address.' });
        return;
    }

    try {
        // Simulate API call (replace with actual backend endpoint)
        await new Promise(resolve => setTimeout(resolve, 500));
        showToast('Thank you for subscribing to our newsletter!', 'success');
        emailInput.value = '';
        Alpine.store('newsletter', { error: '' });
    } catch (error) {
        Alpine.store('newsletter', { error: 'Failed to subscribe. Please try again.' });
    }
}, 300);

document.addEventListener('DOMContentLoaded', () => {
    const footerLinks = document.querySelectorAll('footer a[href^="#"]');
    footerLinks.forEach(link => {
        link.addEventListener('click', (e) => {
            e.preventDefault();
            const targetId = link.getAttribute('href').substring(1);
            const targetElement = document.getElementById(targetId);
            if (targetElement) {
                targetElement.scrollIntoView({ behavior: 'smooth' });
            }
        });
    });

    if (!localStorage.getItem('cookieConsent')) {
        showCookieConsent();
    }
});

function showCookieConsent() {
    const cookieBanner = document.createElement('div');
    cookieBanner.className = 'fixed bottom-0 left-0 right-0 bg-blue-800 text-white p-4 z-50 transform translate-y-full transition-transform duration-300';
    cookieBanner.innerHTML = `
        <div class="container mx-auto flex flex-col md:flex-row items-center justify-between">
            <div class="flex-1 mb-3 md:mb-0">
                <p class="text-sm">
                    We use cookies to improve your experience and provide personalized healthcare services.
                    <a href="#" class="text-blue-300 hover:text-blue-200 hover:underline focus:outline-none focus:ring-2 focus:ring-blue-400 focus:ring-offset-2 focus:ring-offset-blue-800">Learn more</a>
                </p>
            </div>
            <div class="flex space-x-3">
                <button onclick="acceptCookies()" class="btn-standard w-32 h-10 bg-gradient-to-r from-blue-600 to-purple-600 hover:from-blue-700 hover:to-purple-700 rounded-full text-sm font-medium flex items-center justify-center transition-all duration-300 focus:outline-none focus:ring-2 focus:ring-blue-400 focus:ring-offset-2 focus:ring-offset-blue-800">
                    Accept All
                </button>
                <button onclick="rejectCookies()" class="btn-standard w-32 h-10 border border-blue-400 hover:border-blue-300 rounded-full text-sm flex items-center justify-center transition-colors duration-300 focus:outline-none focus:ring-2 focus:ring-blue-400 focus:ring-offset-2 focus:ring-offset-blue-800">
                    Reject
                </button>
            </div>
        </div>
    `;

    document.body.appendChild(cookieBanner);

    setTimeout(() => {
        cookieBanner.classList.remove('translate-y-full');
    }, 100);
}

function acceptCookies() {
    localStorage.setItem('cookieConsent', 'accepted');
    hideCookieBanner();
}

function rejectCookies() {
    localStorage.setItem('cookieConsent', 'rejected');
    hideCookieBanner();
}

function hideCookieBanner() {
    const banner = document.querySelector('.fixed.bottom-0');
    if (banner) {
        banner.classList.add('translate-y-full');
        setTimeout(() => banner.remove(), 300);
    }
}

function showToast(message, type = 'success') {
    const toast = document.createElement('div');
    toast.className = `fixed bottom-4 right-4 px-6 py-3 rounded-lg shadow-lg text-white text-sm z-50 transition-opacity duration-300 ${type === 'success' ? 'bg-green-500' : 'bg-red-500'}`;
    toast.textContent = message;
    document.body.appendChild(toast);
    setTimeout(() => {
        toast.style.opacity = '0';
        setTimeout(() => toast.remove(), 300);
    }, 3000);
}
